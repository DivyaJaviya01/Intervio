document.addEventListener('DOMContentLoaded', function() {
    const profilePics = document.querySelectorAll('.profile-pic');
    
    profilePics.forEach(pic => {
        pic.addEventListener('click', function() {
            const personId = this.getAttribute('data-person');
            const testimonial = testimonials[personId];
            
            // Update active state
            profilePics.forEach(p => {
                p.classList.remove('active');
                p.style.filter = 'brightness(0.6)';
            });
            this.classList.add('active');
            this.style.filter = 'brightness(1)';
            
            // Update content with fade effect
            const textElement = document.querySelector('.testimonial-text');
            const paragraphsElement = document.querySelector('.testimonial-paragraphs');
            
            // Fade out
            textElement.style.opacity = '0';
            paragraphsElement.style.opacity = '0';
            
            setTimeout(() => {
                // Update content
                textElement.textContent = testimonial.text;
                
                // Update paragraphs with logo and person details
                paragraphsElement.innerHTML = testimonial.paragraphs.map((paragraph, index) => `
                    <p class="${index < testimonial.paragraphs.length - 1 ? 'mb-3' : 'mb-3'}" style="color: #000000; font-size: 0.95rem; line-height: 1.6;">
                        ${paragraph}
                    </p>
                `).join('') + `
                    
